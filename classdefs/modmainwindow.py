        t_min_us = dialog_file_import_params.t_min_us
        t_max_us = dialog_file_import_params.t_max_us
        pitch_mm = dialog_file_import_params.pitch_mm

        return provided, name, file_path, t_min_us, t_max_us, pitch_mm

